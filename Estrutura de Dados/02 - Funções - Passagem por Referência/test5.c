#include <stdio.h>

void	func(int *px, int *py)
{
	px = py;
	*py = (*py) * (*px);
	*px = *px + 2;
}

int	main(void)
{
	int	x;
	int	y;

	x = 10;
	y = 20;
	func(&x, &y);
	printf("x = %d, y = %d\n", x, y);
}
